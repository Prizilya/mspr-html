                <p>Coques de macarons (recette sans meringue Italienne) <br>
                    90 g de blancs d’œufs  (séparés 7 jours à l’avance, conservés au réfrigérateur et sorti du réfrigérateur 3 heures avant de préparer les macarons) <br>
                    220 g de sucre glace <br>
                    120 g de poudre d’amandes<br>
                    20 g de sucre en poudre<br>
                    1/2 cuillère à café de colorant en poudre (rouge pour les fraises, jaune pour les pèches / fleur d’oranger)<br>
                    <br>
                    1. Préchauffez le four à 160 °.<br>
                    <br>
                    2. Dans un mixeur, mixer quelques instants la poudre d’amande et le sucre glace pour éviter de les tamiser.<br>
                    <br>
                    3. Monter les blancs en neige ferme avec une pincée de sel. Ajouter progressivement le sucre en poudre et le colorant. Les blancs doivent former des becs d’oiseau quand on enlève le fouet.<br>
                    <br>
                    4. Ajouter le mélange poudre d’amande / sucre glace et mélanger avec une corne (bien plus pratique qu’une maryse) en un mouvement de bas en haut en partant du centre du bol. L’appareil doit être lisse et brillant, il doit faire le ruban sans être liquide, juste souple.<br>
                    <br>
                    5. Remplir une poche a douille lisse avec la préparation, et dresser les macarons sur un tapis en silicone ou du papier cuisson siliconé, déposé sur une grille perforée.<br>
                    <br>
                    6. Enfourner pour 13 à 16 minutes de cuisson, selon votre four et la taille des macarons. Les miens étaient de taille moyenne, et ils ont cuit 16 minutes. Laissez légèrement refroidir avant de retirer les macarons de la feuille.<br>
                    <br>
                    7. Laisser refroidir les coques avant de les garnir.<br>
                    <br>
                    Ganaches montées parfumées<br>
                    50 g de chocolat blanc à pâtisserie (j’utilise la couverture Ivoire Valrhona)<br>
                    25 g de crème fleurette entière<br>
                    3 g de miel<br>
                    75 g de crème fleurette froide<br>
                    1 belle cuillère à café d’arôme (fraise pour les premiers, puis pèche et fleur d’oranger pour les seconds)<br>
                    <br>
                    1. Faire chauffer la crème et le miel et y ajouter l’arôme.<br>
                    <br>
                    2. Y faire fondre le chocolat en morceaux et mélanger pour le faire fondre.<br>
                    <br>
                    3. Ajouter les 75 g de crème restant et mélanger. Conserver au réfrigérateur pour faire refroidir la ganache.<br>
                    <br>
                    4. Quand la ganache est froide, la monter en chantilly puis remplir une poche à douille avec et garnir les macarons.<br>
                    <br>
                    5. Ranger ensuite les macarons au réfrigérateur, ils sont meilleurs au moins deux jours après les avoir assemblés …<br>
                </p>

